# Программа принимает на вход цифру, обозначающую день недели, и проверяет, является ли этот день выходным.
# Пример:
# - 6 -> да
# - 7 -> да
# - 1 -> нет

day_week = int(input('Введите номер дня недели: '))
while day_week < 1 or day_week > 7:
    day_week = int(input('Введите корректный номер дня недели: '))
if day_week == 6 or day_week == 7:
    print(f'- {day_week} -> да')
else:
    print(f'- {day_week} -> нет')
